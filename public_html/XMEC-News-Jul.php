      <h1 class="western" style="page-break-before: always;"><a
 name="Thoughts_From_the_Editor"></a> Thoughts From  the Editor</h1>
                 
      <p class="western" align="justify">As much as I&#8217;m happy that yet another
 novel venture from X-MEC is slowly taking shape, I sincerely regret the
amount  of time taken to make this first edition of X-MEC newsletter a reality.
Although  I can give umpteen number of reasons for us not being able to give
our full  attention to this newsletter, eventually all what counts is whether
we were  able to or not. And we were not able to give X-MEC this newsletter
on time.  And I do, accept the responsibility and apologize for this very
long delay.</p>
               
      <div align="justify"> </div>
               
      <p class="western" align="justify">I do feel that this venture by X-MEC
 is a particularly laudable one and it is for we, x-MECians, to make this
a success. The newsletter should serve as a platform for anything and everything
 that an X-MECian would want to share or convey. It is up to the X-MECian
fraternity to help release this newsletter on a regular basis, so that this
would become yet another feather in the illustrious cap of XMEC. Although
we couldn&#8217;t incorporate everything we had hoped to in this newsletter, I
feel that if some more of you can come forward and devote a little more time
to this small venture, then we can, slowly make this worthwhile. We would
 sincerely like some more of you to provide a few more articles, which could
go a long way in guaranteeing that our newsletter is released on time, with
enough quality. Please, please make it a point to devote some time (a few
hours will do) every few months, and compile something, which you can contribute
to your newsletter. Without your support and cooperation, all this effort
will go futile.</p>
               
      <div align="justify"> </div>
               
      <p class="western" align="justify">I have always been a proud MEC-ian.
 Right from the day of joining that prestigious institution, till date, I
believe that the institution has given me a lot. In a way, I would also say
 that &#8220;MEC made me what I&#8217;m today&#8221; and my college days were &#8220;the best days
of my life&#8221;, for sure. But there&#8217;s no such thing as free lunch. If each of
us could spare some time and write on a sheet of paper the name of persons
to whom we are obliged in some way or other, i.e. the name of persons who
have helped us in our life, who have helped us develop our personality, our
attitude, our skills, our behavior, ourselves, I can assure you (and I&#8217;ve
done this), the list would be much much longer than you think. It could go
on and on, and it should. Now if you could write on another sheet, the name
of persons to whom you have been an inspiration, to whom you&#8217;ve been of worth,
to whom you&#8217;ve been of at least some value, believe me, you&#8217;ll have to think
long and hard to get at least a few names on that sheet. Ok, you guys may
not be &#8220;worse&#8221; like me, and would get quite a few names in the other column,
but the point I wanted to make is that all of us, each and every one of us,
owe a lot, owe a lot to the society, owe a lot to the individuals who helped
develop us, owe a lot to the institution which helped harness our skills.
We have received so much from the society and it&#8217;s our duty to give back
at least a little. How we should do that is for each one of us to decide.
 </p>
               
      <div align="justify"> </div>
               
      <p class="western" align="justify">In this fast moving life, its only
 natural that we lose ourselves in the rapid pace. In fact, the most common
 word I&#8217;ve heard since coming to Bangalore, and of course the one I&#8217;ve most 
 often used also, is that &#8220;I&#8217;m busy, I have no time to spare&#8221;. Like it or
 not, this is how our life is and I believe, would be. But the onus is on
us. Think of making a difference in someone else&#8217;s life, believe that you
can make a difference and you certainly will be remembered. We all can.</p>
               
      <div align="justify"> </div>
               
      <p class="western" align="justify">Wish you all a Merry Xmas and a
very very happy and prosperous 2005.</p>
               
