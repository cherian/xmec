<P class=body><i>Dear Friends,<BR>
 
We had a small (really small !!) gathering of about 12 members for the Alumni General Body Meeting (GBM) in the college on 29th Saturday, chaired by Jithesh M (Vice President) and Sageer (General Secretary) from the Executive Committee. The discussions revolved around the agenda that Sageer had posted last week, the most significant among them being the need to revive the Alumni Exec Committee and its activities. Sageer will be shortly forwarding a brief Note on the details of the discussions.</i></P>
 
<P class=body><i>This was followed by an interaction with the NBA team (7 member committee deputed by AICTE) which lasted about an hour. From the alumni side, we made a small presentation (anchored by myself) about XMECians and our Alumni Association (AAMEC). A copy of the presentation is available at the <a href="downloads.php" class=link> Downloads</a> section for reference.</i></P>
 
<P class=body><i>After the short presentation we had an open house, where the NBA team had specific questions to alumni members under the broad agenda of what we (alumni) believe are the strengths and weaknesses of MEC. At the end of it, the NBA team had a few good suggestions for alumni to take up.
 
1. Contribute to the revision of Curriculum/Syllabi of MEC
2. Anchor some Courses/Guest Sessions on subjects like Entrepreneurship, Basics of Finance & Accounting, Economics and other similar courses. Also, may be start an Entrepreneurship Cell in the college.
3. Contribute to the development of the College (in kind and in cash).</i></P>
 
<P class=body><i>On the whole we had about 25 of us who turned up at the college on 29th, though some of them turned up after the GBM was over. Thanks to all those who helped in making this happen.<BR> 
 
Regards,</i></P>

