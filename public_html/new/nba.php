<P class=body><i>Thanks to all the alumni who took pain in coming down to Cochin and  meeting the NBA team. Renish did a very good presentation about the xmecians, their current positions, Alumni vision, mission, goals acheived, proposed etc. The NBA team was very impressed by this effort and it was the first time they had seen a presentation on behalf of an alumni association, after visits to various colleges all around India. A very special thanks to Renish and all those who contributed in collecting information,bothered to update xmec with info and send in a lot of emails with the info of others  for the beautiful presentation. </i></P>

<P class=body><i>The NBA team had 10 + members, all senior , experienced folk from some of the most prestigious institutions of the country. There was a Professor Emeritus from IIT Mumbai who initiated the talk with us. With him there was 1 professor from IIT Mumbai, IIT Kharagpur, IIT Chennai, NIT Jaipur and a couple of other NITs .They seem to have been assessing colleges/faculty/students for a long time. .As the committee spoke it was evident that they had assessed our faculty carefully and were not happy with their experience and educational qualification. </i></P>
<P class=body><i>The NBA team was of the opinion that Alumni should have a better role in restructuring the syllabus of the college, recruiting able and qualified staff in the college, funding for infrastructural developments, a ladies hostel etc especially since the IHRDE and Govt were not very concerened.
The items listed in the meeting agenda were discussed in the morning session.</i></P>
 
 
<P class=body><i>The goals of Alumni need to be revisited.
Redefine the role of the Alumni in tune with the listed out goals.
The current bylaw seems to be a hindrance in the smooth functioning of the Alumni assosciation. A team has to be formed that will be responsible of amending the bylaw. 
The current bylaw  was discussed at length and some of the suggestions that came are as follows
  i) Network meetings need to be considered as valid meetings
  ii) Resolutions can be passed based on web based polls
  iii) The structure, roles, duties and responsibilities of the executive committee needs to be redefined.
  iv) The post of a Laison officer has to be created, who will be responsible of carrying out the activities connected to college. The laison officer will be given impressed cash so as to carry out activities.
  v) The fund raising will be done based on activities and their plans. The annual income  from the registration of passing out students will be used for organizational activities, site maintanence and other running costs.</i></P>
 
<P class=body><i>A few activities were listed out by the attendees
i) Health insurance schemes for the Alumni, so that Alumni members can be helped based on need
ii) Activities to improve visibility of the Alumni assosiation and college
iii) Instituting scholarship for needy students
iv) Organized helping of placement activities.</i></P>
 
 
<P class=body><i>An annual general body meeting was identified as a must for revitilizing the activities. Holding a meting at the college premises was recognized as impractical as many will find it difficult to come and attend. Banglore was suggested as the venue for the next general body meeting, as Banglore chapter has a pretty good strangth, members from chennai, hyderabad, mumbai, pune, cochin, trivandrum etc could make it. 
The Banglore chapter office bearers have agreed upon conducting the next AGM tentatively on January 11th 2004.
                                        
</i></P>
