      <h1 class="western" style="page-break-before: always;"><a
 name="USA"></a> America for  dummies</h1>
                 
      <p class="western"><b>Musings, observations and anecdotes from a big 
 country</b></p>
                 
      <p class="western" align="right"><b>Sandeep Unni</b></p>
                 
      <p class="western" align="justify">America is a fascinatingly large
 country. A cultural kaleidoscope of 280 million people, home to Whites,
Blacks,  Asian, Indian, Jew, Chinese, Filipino, Japanese, Korean, Vietnamese,
Hawaiian,  Pacific Islander, Mexican, Latino, Hispanic, European, Arabic,
Persian and  combinations of the above races thereof. (Here Indian denotes
the broad envelope  of races that include the American-Indian or the Red
Indian as we know them,  the Indian-American who is American by birth and
preference but Indian by  skin and descent and of course the regular Indian,
by taste and looks.).  This is the land of the Statue of Liberty and the
Grand Canyon, of Martin  Luther King and Mohammed Ali, the land of the best
research and the finest  universities, of more Nobel Prize winners than the
rest of the world put together. Yeh hai America Meri Jaan.  </p>
               
      <div align="justify"> </div>
               
      <p class="western" align="justify">America is the ultimate haven for
 dreamers. It is a land of the self-made man. It is here that some of the
biggest breakthroughs in science unfolded. The country that etched the biggest
success stories in the history of all mankind, where college dropouts and
psychedelics reached iconic role model statures. America is a curious country.
A great land but curious all the same. Here people go to school after attending
college; you wouldn&#8217;t go into the restroom to take rest. A busy signal is
not in a traffic junction at rush hour but in a telephone. Gas is not filled
in cylinders in the kitchen but in cars. Here you can not only pay your bills
by check but also pay your check by cash. A hood is not only in the rhymes
of gangsta&#8217; rap but also in the front of your car. A flash is by no means
an obscene gesture but just a torch. The list goes on. You get the picture.</p>
               
      <div align="justify"> </div>
               
      <p class="western" align="justify">Sometimes I wonder if the entire
 nation is built upon the unyielding backbone of attitude and individuality.
 For an American, there is no further truth than the supreme sense and pride 
 of being an American. Right from when he is born, every human being here
 is constantly reminded and reinforced by his peers and teachers and parents
 and friends and the media that the greatest thing he ever accomplished in
 life was to have been born into this great country. It is a privilege that
 the lesser mortals of the netherworld were not privy to. Now don&#8217;t get me
 wrong and think that I am an anti-nationalist or xenophobe. Some of my best
 friends in this country are Americans; they are some of the nicest people
 around, extremely courteous folks who go out of the way to make you feel
at home. But to this day, the curious phenomenon of the fabled American way
of life never ceases to perplex me.  </p>
               
      <div align="justify"> </div>
               
      <p class="western" align="justify">Another benchmark crucial to the
 American dude is &#8216;space&#8217;. Not the never-ending continuum of the cosmos,
but  the sense of personal and physical being akin to a tiger&#8217;s personal
territory;  which is uniquely his own and only his own. Everyone is entitled
to his &#8216;space&#8217;.  While it is not uncommon for two people to talk at a spit&#8217;s
throw from each  other in India (and giving each other an oral shower in
the process) the American always keeps his distance. The one arm distance
rule is not just in the military. Only the closest confidante or lover has
the right to breach this territory. For the same reason, one can imagine
the look on the face  of the normal John Doe as two Indian langottiya-yaars
walk past him with each other&#8217;s hands-on-shoulder. Mutual respect of each
other&#8217;s space is what defines cool. Some of my yank buddies would go, &#8220;Why
are you guys so touchy-feely? &#8221; Now, how I convinced them that not every
Indian walking hand-on-shoulder with his best friend is a flagrant homosexual
is an effort I will spare you the details of.  </p>
               
      <div align="justify"> </div>
               
      <p class="western" align="justify">I read this book called the &#8216;Notes
 from a Big Country&#8217;, which is probably the funniest book I have read on
the  American way of life. I highly recommend it for any one who appreciates 
a hearty laugh.  The book is filled with innumerable absolutely meaningless
 statistics and inane information unique to America and American behavior.
 And at the end of it, whether it was when you learned that Americans take
 their car wherever they go; even if it's only a block down the street, or
 how everyone is happy to sue each other on basically no grounds at all,
for  no reason or every reason; it provides you countless laugh-out-loud
moments.   </p>
               
      <div align="justify"> </div>
               
      <p class="western" align="justify">If you are one for tickling the
funny bone, another treasure not to be missed is a title called Bush Aphorisms
&#8211; a meticulously chronicled essay of President Bush&#8217;s attempts at speeches
while in office. People the world over enjoyed the humor inlaid in the bloopers.
 Only if Mr. President himself realized they were jokes.  </p>
               
