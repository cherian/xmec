Model Engineering College receives accreditation for...
<BR>
<BR>The undergraduate  courses, Electronics & Communication Engineering and  Electronics & Biomedical Engineering of Model Engineering College has been accredited by AICTE. The acreditation is for 3 years. National Board of Accreditation visited the college in the last week of November, 2003 to evaluate the college. Prof. T K Mani has conveyed the college appreciation to the alumni XMEC for all the support and help during the accreditation process
<BR>Accreditation is a process of quality assurance whereby a programme in an approved institution is critically appraised to verify that the institution or programme meets the Norms and Standards prescribed by the Council from time to time. Accreditation provides quality assurance that the academician aims and objectives of the institution which are honestly pursued and achieved with the help of available resources demonstrating capabilities to ensure continued effectiveness of the educational programme, over the period of accreditation.
<BR>National Board of Accreditation established under section 10(u) of AICTE act 52 of 1987 (an act of parliament of India) is charged with the responsibility of periodically conducting evaluation of technical institutions or programmes on the basis of guidelines, norms and standards specified by it and to make recommendation, regarding recognition or de-recognition of the institution or the programme.

